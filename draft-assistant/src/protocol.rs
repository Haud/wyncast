        let state_update_json = r#"{
            "type": "STATE_UPDATE",
            "timestamp": 123,
            "payload": { "picks": [], "myTeamId": null, "source": "test" }
        }"#;

        let full_sync: ExtensionMessage = serde_json::from_str(full_sync_json).unwrap();
